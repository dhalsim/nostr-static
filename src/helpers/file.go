package helpers

import (
	"encoding/json"
	"io"
	"os"

	"nostr-static/src/types"
)

func SaveNaddrMapping(eventIDToNaddr map[string]string, outputPath string) error {
	file, err := os.Create(outputPath)
	if err != nil {
		return err
	}
	defer file.Close()

	encoder := json.NewEncoder(file)
	encoder.SetIndent("", "  ")
	return encoder.Encode(eventIDToNaddr)
}

